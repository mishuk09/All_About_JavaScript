let a = 20;
let b = 10;

// if,else 
if (a > b) {
    console.log(a + " is greater than " + b);
} else {
    console.log(a + " is not greater than " + b);
}

// switch case

let day = 3;

switch (day) {
    case 1:
        console.log("Sunday");
        break;
    case 2:
        console.log("Monday");
        break;
    case 3:
        console.log("Tuesday");
        break;
    default:
        console.log("Invalid day");
}


//loop

for (let i = 0; i <= 5; i++) {
    console.log(i);
}



let j = 5;
while (j < 20) {
    console.log(j);
    j++;
}