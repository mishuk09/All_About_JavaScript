let a = 20;
let b = 10;

// Arithmetic operators
function Arithmetic() {
    console.log(a + b);
    console.log(a - b);
    console.log(a * b);
    console.log(a / b);
    console.log(a % b);
}
Arithmetic();


// Comparison operators

function Comparison() {
    console.log(a == b);
    console.log(a != b);
    console.log(a > b);
    console.log(a < b);
    console.log(a >= b);
    console.log(a <= b);
}
Comparison();


// Logical operators

function Logical() {
    console.log(true && false);
    console.log(true || false);
    console.log(!a);
}
Logical();
